## Exercise
## Basic while loop

## Instructions
## - Create the variable offset with an initial value of 8.
## - Code a while loop that runs as long as offset is not equal to 0.
## - Inside the while loop:
##   - Print the sentence "correcting...".
##   - Decrease offset by 1.
##   - Print offset to see how it changes.

## MY SOLUTION:

# Initialize offset
offset=8

# Code the while loop
while offset!=0 :
    print ("correcting...")
    offset=offset-1